    private boolean validatePassword() {
        if (userModel.getAccount().isPassTemp()== 1) {
             JOptionPane.showMessageDialog(this,
                "You need to change your password as it is currently temporary!" , 
                "Please change your password" , JOptionPane.PLAIN_MESSAGE);
             return false;
        } else {
            return true;
        }
    }
