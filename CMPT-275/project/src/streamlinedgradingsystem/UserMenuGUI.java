      private void displayWelcomeMessage() {
        if (userModel.getMiddleName().equals("")) {
             welcomeLabel.setText("Welcome " + userModel.getFirstName().trim() + " " 
                    + userModel.getLastName().trim() + " !");
        } else {
            welcomeLabel.setText("Welcome " + userModel.getFirstName().trim() + " "
                    + userModel.getMiddleName().trim() + " " + userModel.getLastName().trim() + " !");
        }
        welcomeLabel.setHorizontalAlignment(SwingConstants.CENTER);
        loginIDLabel.setText("Your Login ID is: " + userModel.getAccount().getLoginID());
        loginIDLabel.setHorizontalTextPosition(SwingConstants.CENTER);
    }
